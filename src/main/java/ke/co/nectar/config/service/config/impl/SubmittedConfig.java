package ke.co.nectar.config.service.config.impl;

public class SubmittedConfig {

    private String key_expiry_no;
    private String encryption_algorithm;
    private String token_carrier_type;
    private String decoder_key_generation_algorithm;
    private String tariff_index;
    private String key_revision_no;
    private String vending_key;
    private String supply_group_code;
    private String key_type;
    private String base_date;
    private String issuer_identification_no;

    public SubmittedConfig() {}

    public String getKey_expiry_no() {
        return key_expiry_no;
    }

    public void setKey_expiry_no(String key_expiry_no) {
        this.key_expiry_no = key_expiry_no;
    }

    public String getEncryption_algorithm() {
        return encryption_algorithm;
    }

    public void setEncryption_algorithm(String encryption_algorithm) {
        this.encryption_algorithm = encryption_algorithm;
    }

    public String getToken_carrier_type() {
        return token_carrier_type;
    }

    public void setToken_carrier_type(String token_carrier_type) {
        this.token_carrier_type = token_carrier_type;
    }

    public String getDecoder_key_generation_algorithm() {
        return decoder_key_generation_algorithm;
    }

    public void setDecoder_key_generation_algorithm(String decoder_key_generation_algorithm) {
        this.decoder_key_generation_algorithm = decoder_key_generation_algorithm;
    }

    public String getTariff_index() {
        return tariff_index;
    }

    public void setTariff_index(String tariff_index) {
        this.tariff_index = tariff_index;
    }

    public String getKey_revision_no() {
        return key_revision_no;
    }

    public void setKey_revision_no(String key_revision_no) {
        this.key_revision_no = key_revision_no;
    }

    public String getVending_key() {
        return vending_key;
    }

    public void setVending_key(String vending_key) {
        this.vending_key = vending_key;
    }

    public String getSupply_group_code() {
        return supply_group_code;
    }

    public void setSupply_group_code(String supply_group_code) {
        this.supply_group_code = supply_group_code;
    }

    public String getKey_type() {
        return key_type;
    }

    public void setKey_type(String key_type) {
        this.key_type = key_type;
    }

    public String getBase_date() {
        return base_date;
    }

    public void setBase_date(String base_date) {
        this.base_date = base_date;
    }

    public String getIssuer_identification_no() {
        return issuer_identification_no;
    }

    public void setIssuer_identification_no(String issuer_identification_no) {
        this.issuer_identification_no = issuer_identification_no;
    }
}
